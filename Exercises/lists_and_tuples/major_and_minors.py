# Mayores y menores

# Dada una lista de números, encontrar el número más grande y el más pequeño en la lista.

numeros = [10, 5, 20, 8, 15]

numero_mas_grande = max(numeros)
numero_mas_pequeno = min(numeros)

print("El número más grande en la lista es:", numero_mas_grande)
print("El número más pequeño en la lista es:", numero_mas_pequeno)