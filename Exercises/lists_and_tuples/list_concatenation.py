# Concatenación de listas

# Crear dos listas con diferentes elementos, luego combinándolas en una sola lista y ordenándolos.

lista1 = [3, 7, 1, 9]
lista2 = [5, 2, 8, 4]

# Combinar las dos listas en una sola lista
lista_combinada = lista1 + lista2

# Ordenar los elementos de la lista combinada
lista_combinada.sort()

# Imprimir la lista combinada ordenada
print("Lista combinada ordenada:", lista_combinada)